package ex_06_Ternary_Operator;

public class Lab063_TO {
    public static void main(String[] args) {

        // Find the maximum number between two numbers
        // X and Y -> Max X, y -> Ternary Operator

        int x = 10;
        int y = 20;
        int max = x > y ? x : y;
        System.out.println(max);

    }
}
