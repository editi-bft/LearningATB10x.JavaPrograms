package ex_04_Operators;

public class Lab036_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        // +
        // -
        // *
        // /
        // %
        int a = 20;
        int b = 3;
        float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);// 6
        System.out.println(a/c);//6.6666665
    }
}
